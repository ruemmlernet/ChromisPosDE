    public boolean checkTerminal() {
        if (!AppConfig.getTse().TseInUse()) return false;
        
        String ts = AppConfig.getTerminalSerial();
        String t2;
        int i = 1;
        boolean ende = false;
        boolean registered = false;
        byte[] tID = AppConfig.getTse().getERSMappings();
        
        while (! ende) {
            t2 = asn1obj.getTerminalID(tID, i);
            if (t2.equals("")) {
                ende = true;
            } else if (t2.equals(ts) ) {
                registered = true;
                ende = true;
            } else {
                i++;
            }
        }
        return registered;
    }
    

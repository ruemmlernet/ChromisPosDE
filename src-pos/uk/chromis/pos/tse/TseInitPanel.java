    private boolean checkPins() {
        boolean b = true;
        if (jTextField5.getText().trim().length() < 4) b = false;
        if (jTextField6.getText().trim().length() < 4) b = false;
        if (jTextField7.getText().trim().length() < 4) b = false;
        if (jTextField8.getText().trim().length() < 4) b = false;
        if (!StringUtils.isNumeric(jTextField5.getText())) b = false;
        if (!StringUtils.isNumeric(jTextField6.getText())) b = false;
        if (!StringUtils.isNumeric(jTextField7.getText())) b = false;
        if (!StringUtils.isNumeric(jTextField8.getText())) b = false;
        if (AppConfig.getTse().TseInUse()) b = false;
        return b;
    }
    

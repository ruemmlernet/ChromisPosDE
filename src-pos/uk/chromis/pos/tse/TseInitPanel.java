    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        TSE.FinishTransactionResult ftr;
        TicketInfo ti;
        for (Long tr: AppConfig.getTse().getOpenTransactions()) {
            TseProcessData.clear();
            TseProcessData.vorgangsTyp = "AVBelegabbruch";
            ftr = AppConfig.getTse().finishTransaction(AppConfig.getTerminalSerial(), 
                                             tr, 
                                             TseProcessData.getProcessData(), 
                                             AppConfig.getTse().getProcesstypeKassenbeleg(),
                                             "");
            if (ftr != null) {
                ti = new TicketInfo();
                ti.setUser(m_App.getAppUserView().getUser().getUserInfo());
                ti.setActiveCash(m_App.getActiveCashIndex());
                ti.setTseData(ftr.logTime, ftr.logTime, Hex.toHexString(ftr.serialNumber), ftr.signatureCounter, 
                              Hex.toHexString(ftr.signatureValue), tr, AppConfig.getTse().getTimeFormat(), 
                              AppConfig.getTse().getSignaturAlgorithmus(), AppConfig.getTse().getCertificationId(), AppConfig.getTse().getPublicKey());
                try {
                    m_dlSales.saveTicketTseFailed(ti);
                } catch (BasicException ex) {
                    Logger.getLogger(TseInitPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        tseOpenTransaction();
    }//GEN-LAST:event_jButton5ActionPerformed


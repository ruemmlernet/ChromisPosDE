    @Override
    public void activate() throws BasicException {

        try {
            con = m_App.getSession().getConnection();
        } catch (SQLException ex) {
            Logger.getLogger(TseInitPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (AppConfig.getTse() == null) {
            jTabbedPane1.setVisible(false);
            
            return;
        }
        if (AppConfig.getTse().TseInUse()) {
            jTextField8.setEnabled(false);
            jTextField7.setEnabled(false);
            jTextField6.setEnabled(false);
            jTextField5.setEnabled(false);
            jButton1.setEnabled(false);
            // Export / löschen
            jTextField4.setEnabled(true);
            jTextField11.setEnabled(true);
            jButton12.setEnabled(true);
            jButton13.setEnabled(true);
        }

        jEditorPane1.setText(AppConfig.getTse().getSerialNumber());
        jEditorPane2.setText(AppConfig.getTerminalSerial());
        if (checkTerminal()) {
            jButton3.setEnabled(false);
            jButton4.setEnabled(true);
        } else {
            jButton3.setEnabled(true);
            jButton4.setEnabled(false);
        }
        tseOpenTransaction();
        
        // Datensicherungs-Daten
        String prop = AppConfig.getInstance().getProperty("tse.backup.dir");
        prop = (prop == null) ? "" : prop;
        jTextField1.setText(prop);
        prop = AppConfig.getInstance().getProperty("tse.backup.file");
        prop = (prop == null) ? "" : prop;
        jTextField2.setText(prop);
        jCheckBox2.setSelected(AppConfig.getInstance().getBoolean("tse.backup.filetimestamp"));
        jCheckBox1.setSelected(AppConfig.getInstance().getBoolean("tse.backup.automatic"));
        jCheckBox3.setSelected(AppConfig.getInstance().getBoolean("tse.backup.currentday"));
        
        // TSE Aktiv-Status
        setTseActiveStateButtons();
    }
    
    private void tseOpenTransaction() {
        // offene Transaktionen
        jEditorPane3.setText("");
        jButton5.setEnabled(false);
        if ((AppConfig.getTse().TseInUse()) && (AppConfig.getTse().getActiveState())) {
            String otListe = "";
            for (Long tr: AppConfig.getTse().getOpenTransactions()) {
                otListe = otListe + String.valueOf(tr) + "\r\n";
            }
            jEditorPane3.setText(otListe);
            if (otListe.length() > 0) {
                jButton5.setEnabled(true);
            }
        }        
    }

